export function ids(id: number) {
  if (id === 98) {
    return process.env.P98!;
  }
  if (id === 99) {
    return process.env.P99!;
  }
  if (id === 131) {
    return process.env.P131!;
  }
  if (id === 104) {
    return process.env.P104!;
  }
  if (id === 129) {
    return process.env.P129!;
  }
}
